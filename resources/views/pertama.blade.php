        <header><h1>Bantu Adaptasi Mahasiswa Baru, ITS Gelar Mentoring Agama Islam
        </h1></header>
        <div class="content">
            <p class="author">Oleh : itsash | 18 | sumber : Online</p>
            <img src="{{ asset('images/gambar.png') }}" alt="lingkungan">
            <p class="kalimatpembuka">Kampus ITS, ITS News — Institut Teknologi Sepuluh Nopember (ITS) kembali menggelar Mentoring Agama Islam Semester Genap 2024/2025 dengan berbagai rangkaian kegiatan. Kegiatan ini bertujuan membentuk karakter mahasiswa baru (maba) muslim berbasis nilai-nilai Islam. Acara yang resmi dibuka dalam kuliah umum ini berlangsung di Masjid Manarul Ilmi ITS, Sabtu (22/02).
            </p>
            <div class="multicolumn">
            <p>Dalam pembukaan acara, Ketua Tim Pembina Kerohanian Islam (TPKI) ITS Prof Dr Muhammad Mashuri MT menyampaikan bahwa mentoring merupakan sarana pencarian ilmu bagi mahasiswa. “Kegiatan mentoring ini untuk mempersiapkan generasi emas di 2045 yang perlu dibekali dengan segudang ilmu, termasuk keilmuan Islam,” ujarnya.
            </p>
            <p>Lebih lanjut, dosen yang akrab disapa Mashuri ini menjelaskan, kegiatan mentoring ini terdiri dari beberapa rangkaian acara. Adapun diawali dengan dengan kuliah umum yang menghadirkan empat materi utama dari dosen-dosen agama Islam ITS sebagai pondasi awal. Setelah mendapatkan pemahaman dasar, mahasiswa mengikuti kelas pendalaman dalam enam sesi yang mencakup diskusi dan ujian untuk menguji pemahaman mereka.
            </p>
            <p>Dosen agama islam ITS Prof Dr Ir Adi Soeprijanto MT saat memberikan materi sirah nabawi pada Kuliah Umum Mentoring Agama Islam Semester Genap 2024/2025
            </p>
            <p>Tak berhenti disitu, kegiatan ini kemudian diperkuat melalui kegiatan ramadan di kampus (RDK) 46. Pada kegiatan ini, maba mengikuti kajian inspiratif, kajian rutin subuh, dna tearawih selama bulan ramadan guna memperdalam pemahaman keislaman secara aplikatif. "melalui mentoring ini, mahasiswa mampu mengamalkan ilmu keislaman dalam kehidupan sehari-hari," tambah dosen Statistika ITS tersebut.</p>
